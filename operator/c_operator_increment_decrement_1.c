// this program demonstrate how unary increment & decrement operator work
// operator which required only one operand called as unary operator

#include<stdio.h>
int main(){
    int var1 = 5;
    var1++;
    printf("%d\n", var1);

    int var2 = 6;
    var2--;
    printf("%d\n",var2);
    return 0;
}